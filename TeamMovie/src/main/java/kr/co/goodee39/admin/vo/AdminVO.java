package kr.co.goodee39.admin.vo;

public class AdminVO {
	
	private int adNum;
	private String adId;
	private String adPw;
	private String adName;
	private int verify;
	
	public int getAdNum() {
		return adNum;
	}
	public void setAdNum(int adNum) {
		this.adNum = adNum;
	}
	public String getAdId() {
		return adId;
	}
	public void setAdId(String adId) {
		this.adId = adId;
	}
	public String getAdPw() {
		return adPw;
	}
	public void setAdPw(String adPw) {
		this.adPw = adPw;
	}
	public String getAdName() {
		return adName;
	}
	public void setAdName(String adName) {
		this.adName = adName;
	}
	public int getVerify() {
		return verify;
	}
	public void setVerify(int verify) {
		this.verify = verify;
	}
}
