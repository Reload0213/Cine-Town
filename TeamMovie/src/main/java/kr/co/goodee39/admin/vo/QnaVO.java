package kr.co.goodee39.admin.vo;

public class QnaVO {
	
	private int qnaNum;
	private String qnaCategory;
	private String qnaTitle;
	private String quaComment;
	private String qnaIsdelete;
	private String qnaRegdate;
	
	public int getQnaNum() {
		return qnaNum;
	}
	public void setQnaNum(int qnaNum) {
		this.qnaNum = qnaNum;
	}
	public String getQnaCategory() {
		return qnaCategory;
	}
	public void setQnaCategory(String qnaCategory) {
		this.qnaCategory = qnaCategory;
	}
	public String getQnaTitle() {
		return qnaTitle;
	}
	public void setQnaTitle(String qnaTitle) {
		this.qnaTitle = qnaTitle;
	}
	public String getQuaComment() {
		return quaComment;
	}
	public void setQuaComment(String quaComment) {
		this.quaComment = quaComment;
	}
	public String getQnaIsdelete() {
		return qnaIsdelete;
	}
	public void setQnaIsdelete(String qnaIsdelete) {
		this.qnaIsdelete = qnaIsdelete;
	}
	public String getQnaRegdate() {
		return qnaRegdate;
	}
	public void setQnaRegdate(String qnaRegdate) {
		this.qnaRegdate = qnaRegdate;
	}
}
