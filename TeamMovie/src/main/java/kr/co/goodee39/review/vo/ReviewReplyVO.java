package kr.co.goodee39.review.vo;

public class ReviewReplyVO {

	private int rpNum;
	private String rpDate;
	private String rpComment;
	private String rpWritername;
	private int rpWriternum;
	private int mvNum;
	private int rpLike;

	public int getRpNum() {
		return rpNum;
	}

	public void setRpNum(int rpNum) {
		this.rpNum = rpNum;
	}

	public String getRpDate() {
		return rpDate;
	}

	public void setRpDate(String rpDate) {
		this.rpDate = rpDate;
	}

	public String getRpComment() {
		return rpComment;
	}

	public void setRpComment(String rpComment) {
		this.rpComment = rpComment;
	}

	public String getRpWritername() {
		return rpWritername;
	}

	public void setRpWritername(String rpWritername) {
		this.rpWritername = rpWritername;
	}

	public int getRpWriternum() {
		return rpWriternum;
	}

	public void setRpWriternum(int rpWriternum) {
		this.rpWriternum = rpWriternum;
	}

	public int getMvNum() {
		return mvNum;
	}

	public void setMvNum(int mvNum) {
		this.mvNum = mvNum;
	}

	public int getRpLike() {
		return rpLike;
	}

	public void setRpLike(int rpLike) {
		this.rpLike = rpLike;
	}

}
